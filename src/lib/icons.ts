/** ★☆☆ のような星文字列を作る（最大3） */
export function difficultyStars(d: Difficulty): string {
  const n = DIFFICULTY_THEME[d].stars
  return '★★★'.slice(0, n) + '☆☆☆'.slice(0, 3 - n)
}

